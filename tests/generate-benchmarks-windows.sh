#!/usr/bin/env bash
set -euo pipefail

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
GRAY='\033[0;90m'
NC='\033[0m'

ROOT_DIR=$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)
STAGE_DIR=""

run() {
  printf >&2 "${GRAY}%s >${NC} " "$(pwd)"
  printf >&2 "${YELLOW}"
  printf >&2 "%q " "$@"
  printf >&2 "${NC}\n"

  "$@"
  local exit_code=$?
  if [[ $exit_code -ne 0 ]]; then
    echo -e >&2 "${RED}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
    echo -e >&2 "${RED}[ERROR]${NC} Command failed with exit code ${exit_code}: ${YELLOW}$1${NC}"
    echo -e >&2 "${RED}        Full command:${NC} $*"
    echo -e >&2 "${RED}        Working dir:${NC} $(pwd)"
    echo -e >&2 "${RED}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
    return $exit_code
  fi
}

fail() {
  echo -e >&2 "${RED}[ERROR]${NC} $*"
  exit 1
}

cleanup() {
  if [[ -n "${STAGE_DIR}" ]] && [[ -d "${STAGE_DIR}" ]]; then
    rm -rf "${STAGE_DIR}"
  fi
}
trap cleanup EXIT

require_file() {
  local path=$1
  [[ -s "${path}" ]] || fail "missing or empty result file: ${path}"
}

row_count() {
  awk 'NR > 1 { count++ } END { print count + 0 }' "$1"
}

require_exact_row() {
  local file=$1
  local expected_count=$2
  shift 2

  local actual_count
  actual_count=$(awk -F, -v expected="${expected_count}" -v c1="${1}" -v v1="${2}" \
    -v c2="${3:-}" -v v2="${4:-}" '
      NR == 1 { next }
      {
        ok = 1
        if (c1 != "" && $c1 != v1) ok = 0
        if (c2 != "" && $c2 != v2) ok = 0
        if (ok) count++
      }
      END { print count + 0 }' "${file}")

  [[ "${actual_count}" == "${expected_count}" ]] || fail "unexpected row count in ${file} for filter ${*}: got=${actual_count} expected=${expected_count}"
}

scenario_label() {
  case "$1" in
    0) printf 'max\n' ;;
    100000) printf '100k/s\n' ;;
    10000) printf '10k/s\n' ;;
    *) fail "unknown target_rps for scenario label: $1" ;;
  esac
}

mode_label() {
  case "$1" in
    c-npipe) printf 'named-pipe\n' ;;
    c-shm-hybrid) printf 'shm-hybrid\n' ;;
    *) fail "unknown windows benchmark mode: $1" ;;
  esac
}

render_windows_markdown() {
  local csv_file=$1
  local output_path=$2
  local targets=(0 100000 10000)
  local modes=(c-npipe c-shm-hybrid)

  {
    printf '# Windows Benchmarks\n\n'
    printf 'This file is generated by `tests/generate-benchmarks-windows.sh`.\n'
    printf 'Do not edit it manually.\n\n'
    printf 'Source benchmark script:\n'
    printf -- '- `tests/run-live-win-profile-bench.sh`\n\n'

    printf '## Windows C Transport Profiles\n\n'
    printf '| Scenario | Mode | Throughput (req/s) | p50 (us) | Client CPU (cores) |\n'
    printf '| --- | --- | ---: | ---: | ---: |\n'
    local target_rps mode row scenario duration _requests _responses _mismatches throughput p50 _p95 _p99 client_cpu
    for target_rps in "${targets[@]}"; do
      scenario=$(scenario_label "${target_rps}")
      for mode in "${modes[@]}"; do
        row=$(awk -F, -v m="${mode}" -v t="${target_rps}" 'NR > 1 && $1 == m && $3 == t { print; exit }' "${csv_file}")
        IFS=, read -r _mode duration _target _requests _responses _mismatches throughput p50 _p95 _p99 client_cpu <<<"${row}"
        printf '| %s | %s | %s | %s | %s |\n' \
          "${scenario}" "$(mode_label "${mode}")" "${throughput}" "${p50}" "${client_cpu}"
      done
    done
  } > "${output_path}"
}

main() {
  local csv_file
  local output_tmp
  local targets=(0 100000 10000)
  local modes=(c-npipe c-shm-hybrid)
  local target_rps mode

  STAGE_DIR=$(mktemp -d "${ROOT_DIR}/.benchmarks-windows.XXXXXX")
  csv_file="${STAGE_DIR}/windows.csv"
  output_tmp="${STAGE_DIR}/benchmarks-windows.md"

  cd "${ROOT_DIR}"
  run env NETIPC_RESULTS_FILE="${csv_file}" "${ROOT_DIR}/tests/run-live-win-profile-bench.sh"

  require_file "${csv_file}"
  [[ "$(row_count "${csv_file}")" == "6" ]] || fail "Windows benchmark matrix is incomplete"

  for target_rps in "${targets[@]}"; do
    for mode in "${modes[@]}"; do
      require_exact_row "${csv_file}" 1 1 "${mode}" 3 "${target_rps}"
    done
  done

  render_windows_markdown "${csv_file}" "${output_tmp}"
  run mv "${output_tmp}" "${ROOT_DIR}/benchmarks-windows.md"

  echo -e "${GREEN}Generated benchmarks-windows.md from a complete staged benchmark run.${NC}"
}

main "$@"
