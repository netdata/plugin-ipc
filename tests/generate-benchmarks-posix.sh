#!/usr/bin/env bash
set -euo pipefail

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
GRAY='\033[0;90m'
NC='\033[0m'

ROOT_DIR=$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)
STAGE_DIR=""

run() {
  printf >&2 "${GRAY}%s >${NC} " "$(pwd)"
  printf >&2 "${YELLOW}"
  printf >&2 "%q " "$@"
  printf >&2 "${NC}\n"

  "$@"
  local exit_code=$?
  if [[ $exit_code -ne 0 ]]; then
    echo -e >&2 "${RED}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
    echo -e >&2 "${RED}[ERROR]${NC} Command failed with exit code ${exit_code}: ${YELLOW}$1${NC}"
    echo -e >&2 "${RED}        Full command:${NC} $*"
    echo -e >&2 "${RED}        Working dir:${NC} $(pwd)"
    echo -e >&2 "${RED}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
    return $exit_code
  fi
}

fail() {
  echo -e >&2 "${RED}[ERROR]${NC} $*"
  exit 1
}

cleanup() {
  if [[ -n "${STAGE_DIR}" ]] && [[ -d "${STAGE_DIR}" ]]; then
    rm -rf "${STAGE_DIR}"
  fi
}
trap cleanup EXIT

require_file() {
  local path=$1
  [[ -s "${path}" ]] || fail "missing or empty result file: ${path}"
}

row_count() {
  awk 'NR > 1 { count++ } END { print count + 0 }' "$1"
}

require_exact_row() {
  local file=$1
  local expected_count=$2
  shift 2

  local actual_count
  actual_count=$(awk -F, -v expected="${expected_count}" -v c1="${1}" -v v1="${2}" \
    -v c2="${3:-}" -v v2="${4:-}" -v c3="${5:-}" -v v3="${6:-}" '
      NR == 1 { next }
      {
        ok = 1
        if (c1 != "" && $c1 != v1) ok = 0
        if (c2 != "" && $c2 != v2) ok = 0
        if (c3 != "" && $c3 != v3) ok = 0
        if (ok) count++
      }
      END { print count + 0 }' "${file}")

  [[ "${actual_count}" == "${expected_count}" ]] || fail "unexpected row count in ${file} for filter ${*}: got=${actual_count} expected=${expected_count}"
}

render_posix_markdown() {
  local uds_csv=$1
  local shm_csv=$2
  local negotiated_csv=$3
  local output_path=$4
  local scenarios=(max 100k/s 10k/s)
  local languages=(c rust go)
  local shm_languages=(c rust)
  local profiles=(profile1-uds profile2-shm)

  {
    printf '# POSIX Benchmarks\n\n'
    printf 'This file is generated by `tests/generate-benchmarks-posix.sh`.\n'
    printf 'Do not edit it manually.\n\n'
    printf 'Source benchmark scripts:\n'
    printf -- '- `tests/run-live-uds-bench.sh`\n'
    printf -- '- `tests/run-live-shm-bench.sh`\n'
    printf -- '- `tests/run-negotiated-profile-bench.sh`\n\n'

    printf '## UDS Matrix\n\n'
    printf '| Scenario | Client | Server | Throughput (req/s) | p50 (us) | Client CPU (cores) | Server CPU (cores) | Total CPU (cores) |\n'
    printf '| --- | --- | --- | ---: | ---: | ---: | ---: | ---: |\n'
    for scenario in "${scenarios[@]}"; do
      for client in "${languages[@]}"; do
        for server in "${languages[@]}"; do
          local row
          row=$(awk -F, -v s="${scenario}" -v c="${client}" -v srv="${server}" \
            'NR > 1 && $1 == s && $2 == c && $3 == srv { print; exit }' "${uds_csv}")
          IFS=, read -r _scenario _client _server throughput p50 client_cpu server_cpu total_cpu <<<"${row}"
          printf '| %s | %s | %s | %s | %s | %s | %s | %s |\n' \
            "${_scenario}" "${_client}" "${_server}" "${throughput}" "${p50}" "${client_cpu}" "${server_cpu}" "${total_cpu}"
        done
      done
    done
    printf '\n'

    printf '## SHM Hybrid Matrix\n\n'
    printf '| Scenario | Client | Server | Throughput (req/s) | p50 (us) | Client CPU (cores) | Server CPU (cores) | Total CPU (cores) |\n'
    printf '| --- | --- | --- | ---: | ---: | ---: | ---: | ---: |\n'
    for scenario in "${scenarios[@]}"; do
      for client in "${shm_languages[@]}"; do
        for server in "${shm_languages[@]}"; do
          local row
          row=$(awk -F, -v s="${scenario}" -v c="${client}" -v srv="${server}" \
            'NR > 1 && $1 == s && $2 == c && $3 == srv { print; exit }' "${shm_csv}")
          IFS=, read -r _scenario _client _server throughput p50 client_cpu server_cpu total_cpu <<<"${row}"
          printf '| %s | %s | %s | %s | %s | %s | %s | %s |\n' \
            "${_scenario}" "${_client}" "${_server}" "${throughput}" "${p50}" "${client_cpu}" "${server_cpu}" "${total_cpu}"
        done
      done
    done
    printf '\n'

    printf '## Negotiated Profiles\n\n'
    printf '| Scenario | Profile | Throughput (req/s) | p50 (us) | Client CPU (cores) | Server CPU (cores) | Total CPU (cores) |\n'
    printf '| --- | --- | ---: | ---: | ---: | ---: | ---: |\n'
    for scenario in "${scenarios[@]}"; do
      for profile in "${profiles[@]}"; do
        local row
        row=$(awk -F, -v s="${scenario}" -v p="${profile}" \
          'NR > 1 && $1 == s && $2 == p { print; exit }' "${negotiated_csv}")
        IFS=, read -r _scenario _profile throughput p50 client_cpu server_cpu total_cpu <<<"${row}"
        printf '| %s | %s | %s | %s | %s | %s | %s |\n' \
          "${_scenario}" "${_profile}" "${throughput}" "${p50}" "${client_cpu}" "${server_cpu}" "${total_cpu}"
      done
    done
  } > "${output_path}"
}

main() {
  local uds_csv
  local shm_csv
  local negotiated_csv
  local output_tmp

  STAGE_DIR=$(mktemp -d "${ROOT_DIR}/.benchmarks-posix.XXXXXX")
  uds_csv="${STAGE_DIR}/uds.csv"
  shm_csv="${STAGE_DIR}/shm.csv"
  negotiated_csv="${STAGE_DIR}/negotiated.csv"
  output_tmp="${STAGE_DIR}/benchmarks-posix.md"

  cd "${ROOT_DIR}"

  run env NETIPC_RESULTS_FILE="${uds_csv}" "${ROOT_DIR}/tests/run-live-uds-bench.sh"
  run env NETIPC_RESULTS_FILE="${shm_csv}" "${ROOT_DIR}/tests/run-live-shm-bench.sh"
  run env NETIPC_RESULTS_FILE="${negotiated_csv}" "${ROOT_DIR}/tests/run-negotiated-profile-bench.sh"

  require_file "${uds_csv}"
  require_file "${shm_csv}"
  require_file "${negotiated_csv}"

  [[ "$(row_count "${uds_csv}")" == "27" ]] || fail "UDS benchmark matrix is incomplete"
  [[ "$(row_count "${shm_csv}")" == "12" ]] || fail "SHM benchmark matrix is incomplete"
  [[ "$(row_count "${negotiated_csv}")" == "6" ]] || fail "Negotiated profile benchmark matrix is incomplete"

  local scenarios=(max 100k/s 10k/s)
  local languages=(c rust go)
  local shm_languages=(c rust)
  local profiles=(profile1-uds profile2-shm)

  local scenario client server profile
  for scenario in "${scenarios[@]}"; do
    for client in "${languages[@]}"; do
      for server in "${languages[@]}"; do
        require_exact_row "${uds_csv}" 1 1 "${scenario}" 2 "${client}" 3 "${server}"
      done
    done

    for client in "${shm_languages[@]}"; do
      for server in "${shm_languages[@]}"; do
        require_exact_row "${shm_csv}" 1 1 "${scenario}" 2 "${client}" 3 "${server}"
      done
    done

    for profile in "${profiles[@]}"; do
      require_exact_row "${negotiated_csv}" 1 1 "${scenario}" 2 "${profile}"
    done
  done

  render_posix_markdown "${uds_csv}" "${shm_csv}" "${negotiated_csv}" "${output_tmp}"
  run mv "${output_tmp}" "${ROOT_DIR}/benchmarks-posix.md"

  echo -e "${GREEN}Generated benchmarks-posix.md from a complete staged benchmark run.${NC}"
}

main "$@"
